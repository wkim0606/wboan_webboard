<?php
/* =============================================
   DB 연결 설정
============================================= */
$host = "192.168.20.130";
$user = "root";
$pass = "1234";          // 본인의 MySQL 비밀번호
$db   = "webboard";

/* =============================================
   디버깅 플래그 (개발 중: true / 운영 시: false)
============================================= */
$DEBUG = true;

/* =============================================
   mysqli 연결
============================================= */
$conn = @new mysqli($host, $user, $pass, $db);

/* =============================================
   DB 연결 에러 체크 (디버깅용)
============================================= */
if ($conn->connect_errno) {

    if ($DEBUG) {
        echo "<h3>🚨 DB 연결 실패(Debug Mode)</h3>";
        echo "에러코드: " . $conn->connect_errno . "<br>";
        echo "에러메시지: " . $conn->connect_error . "<br>";
        echo "Host: $host<br>";
        echo "User: $user<br>";
        echo "Database: $db<br>";
        exit;  // 연결 실패 시 종료
    } else {
        die("데이터베이스 연결에 문제가 발생했습니다.");
    }
} else {
    if ($DEBUG) {
        echo "<p style='color:green;'>✅ DB 연결 성공(Debug Mode): $db</p>";
    }
}

/* =============================================
   문자셋 설정
============================================= */
$conn->set_charset("utf8mb4");

?>

